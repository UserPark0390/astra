package Broze;

import java.util.Scanner;

public class BackJoon10950 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int N = sc.nextInt();

		while (N-- > 0) {
			int A = sc.nextInt();
			int B = sc.nextInt();

			System.out.println(A + B);

		}
		sc.close();
	}
}
